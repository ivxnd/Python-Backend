# Dividir listas con split() (parsing)
cadena = 'Hola Mundo'
palabras = cadena.split()
print(palabras)

# Buscar con find()
posicion = cadena.find('Mundo')
print(f'Posici√≥n de la cadena Mundo: {posicion}')

# Reemplazar con replace()
nueva_cadena = cadena.replace('Mundo', 'GitHub')
print(f'Nueva cadena reemplazada: {nueva_cadena}')

# Limpiar una cadena con strip()
cadena = '   Hola Mundo   '
cadena_limpia = cadena.strip()
print(f'Cadena limpia: {cadena_limpia}')

